package ru.ramil.Calculator;

public class Calculator {
    public int add(int a, int b) {
        return a + b;
    }

    public int dif(int a, int b) {
        return a - b;
    }

    public float div (int a, int b) {
        return (float)a / b;
    }

    public int mul(int a, int b) {
        return a * b;
    }
}
